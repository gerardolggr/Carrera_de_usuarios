    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try{
            //
            if (cajaRegistro .getText().isEmpty()
                || cajaRegistro.getText().contains("Ingrese") 
                || cajaRegistro.getText().contains(" ")) {
                JOptionPane.showMessageDialog(null, "El campo no puede estar vac√≠o");
            }else if(contador < 5){
                corredor = new ThreadRunner(cajaRegistro.getText());
                corredores.add(corredor);
                contador++;
                JOptionPane.showMessageDialog(null, "El corredore ha sido registrado");
            }else{
                JOptionPane.showMessageDialog(null, "Lista llena");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "El el pograma ha fallado");
        }
        cajaRegistrados.setText("");
        for(Runner c:corredores){
            cajaRegistrados.append(c.getName() + "\n");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnReniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReniciarActionPerformed
        cajaRegistrados.setText("");
        cajaResultados.setText("");
        corredores.removeAll(corredores);
        contador = 0;
    }//GEN-LAST:event_btnReniciarActionPerformed

    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        if (contador < 5) {
            JOptionPane.showMessageDialog(null, "Faltan " + (5 - contador) + " por registrar.");
        }else{
            for(Runner c:corredores){
                cajaResultados.append(c.getName() + ", tiempo: " + c.getSpeed() + " segundos" +"\n");
            }  
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

